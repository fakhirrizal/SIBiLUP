						<p>Sistem Informasi Pembinaan Kelembagaan Urusan Perumahan (SIBiLUP) merupakan suatu sistem yang terintegrasi dan saling berinteraksi dari seluruh aktivitas dalam lingkup kegiatan yang ada di lingkungan Direktorat Jenderal Penyediaan Perumahan. SIBiLUP ini merupakan sistem berbasis web dan merupakan situs resmi yang dikelola oleh Direktorat Perencanaan Penyediaan Perumahan. Direktorat Jenderal Penyediaan Perumahan, Kementerian Pekerjaan Umum dan Perumahan Rakyat.</p>
                        <p>SIBiLUP dikembangkan sebagai sebuah sarana pengelolaan informasi dan pengetahuan bidang penyediaan perumahan yang mudah diakses oleh berbagai pihak. Sistem ini juga sebagai sarana untuk membangun komunikasi antar semua pihak yang terkait dengan upaya penyediaan perumahan, baik tingkat pusat, provinsi, maupun kabupaten/kota dan sarana koordinasi secara virtual antara pemerintah pusat dengan pemerintah daerah provinsi atau kabupaten/kota. Selain itu SIBiLUP juga akan berfungsi sebagai sarana pemantauan kinerja penyelenggaraan urusan bidang perumahan antar daerah (provinsi atau kabupaten/kota).</p>
                        <p>SIBiLUP ini ke depannya diharapkan dapat menjadi salah satu alat bantu dalam menilai atau mengukur kinerja lembaga daerah dalam penyelenggaraan urasan PKP, sehingga nantinya dapat dijadikan sebagai rujukan dalam pemberian penghargaan atau intensif dan disinsentif yang tepat bagi lembaga bidang PKP di daerah.</p>
						<h4>Manfaat SIBiLUP</h4>
						<div dir="ltr"><br></div>
						<p>Manfaat yang diharapkan dengan adanya SIBiLUP bagi pemerintah daerah, antara lain:</p>
						<div dir="ltr"></div>
						<ol class="decimal">
							<li>Terjadi peningkatan pengetahuan dari SDM atau personil di OPD bidang PKP tentang bidang PKP</li>
							<li>Terciptanya SDM OPD bidang PKP yang kompeten dalam menangani permasalahan bidang PKP di daerah</li>
							<li>Terjadinya komunikasi dan koordinasi yang aktif dan intensif antara Pemrintah Pusat dengan Pemerintah Daerah</li>
							<li>Terjadi internalisasi pengetahuan bidang PKP di lingkungan OPD bidang PKP</li>
							<li>Terjadinya peningkatan capaian kinerja OPD bidang PKP, khususnya terkait dengan perencanaan penyediaan perumahan<p>&nbsp;</p></li>
						</ol>
